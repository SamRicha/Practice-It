/*
 *  Write a method named averageOf3 that accepts three integers as parameters and returns the 
 *  average of the three integers as a real number. For example, the call of averageOf3(4, 7, 13) should return 8.
 */
 
function averageOf3(num1, num2, num3){
    var result = (num1 + num2 + num3)/ 3;
    return result;
}
